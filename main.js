function reveal() {
    var x = document.getElementById("show");
    var y = document.getElementById("change");

    if (x.style.display === "none") {
        x.style.display = "grid";
        y.style.filter= "brightness(0%)";
        y.style.backgroundImage="url(assets/images/icon-minus.svg)";
    } 
    else {
        x.style.display = "none";
        y.style.filter="none";
        y.style.backgroundImage="none";
    }
}

